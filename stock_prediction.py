import yfinance as yf
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from sklearn.preprocessing import MinMaxScaler
from sklearn.metrics import mean_squared_error, mean_absolute_error
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import LSTM, Dense, Dropout
from tensorflow.keras.callbacks import EarlyStopping

"""###Data Acquisition"""

# Choosing the stock ticker ('TSLA' for Tesla)
ticker = 'TSLA'

# Download historical data (last 5 years)
data = yf.download(ticker, start='2018-01-01', end='2023-01-01')

# Display the first few rows of data
print(data.head())

"""###Data Preprocessing"""

# Handle Missing Values
data.fillna(method='ffill', inplace=True)

# Feature Selection
data_close = data[['Close']]

# Data Normalization
scaler = MinMaxScaler(feature_range=(0, 1))
data_scaled = scaler.fit_transform(data_close)

# Creating Sequences for LSTM
def create_sequences(data, seq_length):
    sequences = []
    labels = []
    for i in range(seq_length, len(data)):
        sequences.append(data[i-seq_length:i, 0])  # past 'seq_length' days
        labels.append(data[i, 0])  # next day's closing price
    return np.array(sequences), np.array(labels)

seq_length = 60  # 60 days look-back window
X, y = create_sequences(data_scaled, seq_length)

# Split data into train and test sets (80% train, 20% test)
train_size = int(len(X) * 0.8)
X_train, X_test = X[:train_size], X[train_size:]
y_train, y_test = y[:train_size], y[train_size:]

# Reshape input to match LSTM input shape (samples, timesteps, features)
X_train = X_train.reshape((X_train.shape[0], X_train.shape[1], 1))
X_test = X_test.reshape((X_test.shape[0], X_test.shape[1], 1))

"""###Model Design"""

# Build LSTM model
model = Sequential()

# Add LSTM layer
model.add(LSTM(units=50, return_sequences=True, input_shape=(X_train.shape[1], 1)))
model.add(Dropout(0.2))  # Dropout for regularization

# Add second LSTM layer
model.add(LSTM(units=50, return_sequences=False))
model.add(Dropout(0.2))

# Output layer (Dense layer)
model.add(Dense(units=1))  # Predict next day's closing price

# Compile the model
model.compile(optimizer='adam', loss='mean_squared_error')

"""###Training and validation"""

# Early stopping to prevent overfitting
early_stopping = EarlyStopping(monitor='val_loss', patience=10, restore_best_weights=True)

# Train the model
history = model.fit(X_train, y_train, epochs=50, batch_size=32, validation_data=(X_test, y_test), callbacks=[early_stopping])

"""###Evalutation"""

# Make predictions
y_pred = model.predict(X_test)

# Inverse transform the predictions and actual values to get the original scale
y_pred_actual = scaler.inverse_transform(y_pred)
y_test_actual = scaler.inverse_transform(y_test.reshape(-1, 1))

# Calculate MSE and MAE
mse = mean_squared_error(y_test_actual, y_pred_actual)
mae = mean_absolute_error(y_test_actual, y_pred_actual)

# Print evaluation metrics
print(f'Mean Squared Error (MSE): {mse}')
print(f'Mean Absolute Error (MAE): {mae}')
from sklearn.metrics import r2_score

# Calculate R^2 score
r2 = r2_score(y_test_actual, y_pred_actual)

# Print R^2 value
print(f'R2 Score: {r2}')

"""###Visualising the results"""

# Plot Data Trends (Stock Price Trend)
plt.figure(figsize=(14,7))
plt.plot(data['Close'], color='blue', label=f'{ticker} Stock Price')
plt.title(f'{ticker} Stock Price Trend (2018-2023)')
plt.xlabel('Date')
plt.ylabel('Stock Price')
plt.legend()
plt.show()

# Plot Training Progress (Loss during Training)
plt.figure(figsize=(14,7))
plt.plot(history.history['loss'], label='Training Loss')
plt.plot(history.history['val_loss'], label='Validation Loss')
plt.title('Model Loss during Training')
plt.xlabel('Epochs')
plt.ylabel('Loss')
plt.legend()
plt.show()

# Plot Predicted vs Actual Prices
plt.figure(figsize=(14,7))
plt.plot(y_test_actual, color='blue', label='Actual Stock Price')
plt.plot(y_pred_actual, color='red', label='Predicted Stock Price')
plt.title(f'{ticker} Stock Price Prediction')
plt.xlabel('Time')
plt.ylabel('Stock Price')
plt.legend()
plt.show()

# Create a summary of the results
results = pd.DataFrame({
    'Metric': ['MSE', 'MAE', 'R2 Score'],
    'Value': [mse, mae, r2]
})

# Save to a CSV file inside the container
results.to_csv('prediction_results.csv', index=False)
print("Results saved to prediction_results.csv")

